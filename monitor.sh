#!/bin/bash

echo "======================================================"
echo "         AWS EC2 SYSTEM MONITORING REPORT"
echo "======================================================"
echo "Generated on: $(date)"
echo "Hostname: $(hostname)"
echo

echo "======================================================"
echo "RUNNING SERVICES"
echo "======================================================"
systemctl list-units --type=service --state=running --no-pager

echo
echo "======================================================"
echo "CPU USAGE"
echo "======================================================"
top -bn1 | grep "Cpu(s)"

echo
echo "======================================================"
echo "MEMORY USAGE"
echo "======================================================"
free -h

echo
echo "======================================================"
echo "DISK USAGE"
echo "======================================================"
df -h

echo
echo "======================================================"
echo "REPORT COMPLETED"
echo "======================================================"
